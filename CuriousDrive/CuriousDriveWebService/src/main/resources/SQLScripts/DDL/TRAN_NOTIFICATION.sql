CREATE TABLE IF NOT EXISTS `TRAN_NOTIFICATION` (
    `NOTIFICATION_ID` INT(11) NOT NULL AUTO_INCREMENT,
    `QUESTION_ID` INT(11) NULL,
    `FROM_USER_ID` INT(11) NULL,
	`TO_USER_ID` INT(11) NULL,
	`SUBSYSTEM_ID` INT(11) NULL,
	`SUBSYSTEM_VALUE` VARCHAR(50) NULL,
	`SUBSYSTEM_REFERENCE_ID` INT(11) NULL,
    `POINT_ID` INT(11) NULL,
    `MESSAGE_ID` INT(11) NULL,
    `IS_READ` VARCHAR(5) NULL,
    `IS_EMAIL` VARCHAR(5) NULL,
    `EMAIL_SENT_DATE` DATETIME DEFAULT NULL,
    `CREATED_BY` INT(11) DEFAULT NULL,
	`CREATED_DATE` DATETIME DEFAULT NULL,
    `MODIFIED_BY` INT(11) DEFAULT NULL,
	`MODIFIED_DATE` DATETIME DEFAULT NULL,
    `UPDATE_SEQ` INT(11) DEFAULT NULL,
    PRIMARY KEY (`NOTIFICATION_ID`)
)  ENGINE=InnoDB DEFAULT CHARSET=latin1;
